# -*- coding:utf-8 -*-
class calc2:
    '''
    計算を行うクラスです
    '''
    def mul_(a, b):
        '''
        掛け算を行う

        Parameters
        ----------
        a: float
        b: float

        Returns
        -------
        mul_: float
        '''

        mul_ = a * b
        return mul_

    def div_(a, b):
        '''
        割り算を行う

        Parameters
        ----------
        a: float
        b: float

        Returns
        -------
        div_: float
        '''

        div_ = a / b
        return div_
