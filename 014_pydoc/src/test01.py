# -*- coding:utf-8 -*-
class calc:
    '''
    計算を行うクラスです
    '''
    def sum_(a, b):
        '''
        足し算を行う

        Parameters
        ----------
        a: float
        b: float

        Returns
        -------
        sum_: float
        '''

        sum_ = a + b
        return sum_

    def sub_(a, b):
        '''
        引き算を行う

        Parameters
        ----------
        a: float
        b: float

        Returns
        -------
        sub_: float
        '''

        sub_ = a - b

        return sub_
